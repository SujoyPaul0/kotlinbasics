fun main() {
    val trafficLightColor = "Black"
    
    if (trafficLightColor == "Red") {
        println("Stop")
    } else if (trafficLightColor == "Yellow") {
        println("slow")
    } else if (trafficLightColor == "Green") {
        println("go")
    } else {
        println("Invalid color")
    }
}